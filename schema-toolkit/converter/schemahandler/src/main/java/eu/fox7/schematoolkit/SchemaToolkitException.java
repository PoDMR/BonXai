package eu.fox7.schematoolkit;

public class SchemaToolkitException extends Exception {

	private static final long serialVersionUID = 1L;

	public SchemaToolkitException() {
	}

	public SchemaToolkitException(String message) {
		super(message);
	}

	public SchemaToolkitException(Throwable cause) {
		super(cause);
	}

	public SchemaToolkitException(String message, Throwable cause) {
		super(message, cause);
	}

}
